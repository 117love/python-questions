# ******************************************************************************
#   問題 2: 条件分岐（if文）                                                       
#                                                                             
#   変数 score が 60 以上なら「合格」と表示し、それ以外なら「不合格」と表示してください。
# ******************************************************************************

 
score = 60

if score > 60:     
    print("合格")
else:
    print("不合格")    
score = 75  # 他の数値に変えてもOK


# 出力例: 合格
